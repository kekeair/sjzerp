package com.qxh.model;

public class StockRecord {
	
	private int atNo;
	private int centerId;
	private int billId;
	private int goodsId;
	private int goodsType;
	private String acrossTime;
	private double acrossNum;
	private double acrossPrice;
	private double acrossMoney;
	private int stockFlag;
	private double stockNum;
	private double stockPrice;
	
	private String billCode;
	private String goodsNm;
	
	public int getAtNo() {
		return atNo;
	}
	public void setAtNo(int atNo) {
		this.atNo = atNo;
	}
	public int getCenterId() {
		return centerId;
	}
	public void setCenterId(int centerId) {
		this.centerId = centerId;
	}
	public int getBillId() {
		return billId;
	}
	public void setBillId(int billId) {
		this.billId = billId;
	}
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public int getGoodsType() {
		return goodsType;
	}
	public void setGoodsType(int goodsType) {
		this.goodsType = goodsType;
	}
	public String getAcrossTime() {
		return acrossTime;
	}
	public void setAcrossTime(String acrossTime) {
		this.acrossTime = acrossTime;
	}
	public double getAcrossNum() {
		return acrossNum;
	}
	public void setAcrossNum(double acrossNum) {
		this.acrossNum = acrossNum;
	}
	public double getAcrossPrice() {
		return acrossPrice;
	}
	public void setAcrossPrice(double acrossPrice) {
		this.acrossPrice = acrossPrice;
	}
	public double getAcrossMoney() {
		return acrossMoney;
	}
	public void setAcrossMoney(double acrossMoney) {
		this.acrossMoney = acrossMoney;
	}
	public int getStockFlag() {
		return stockFlag;
	}
	public void setStockFlag(int stockFlag) {
		this.stockFlag = stockFlag;
	}
	public double getStockNum() {
		return stockNum;
	}
	public void setStockNum(double stockNum) {
		this.stockNum = stockNum;
	}
	public double getStockPrice() {
		return stockPrice;
	}
	public void setStockPrice(double stockPrice) {
		this.stockPrice = stockPrice;
	}
	public String getBillCode() {
		return billCode;
	}
	public void setBillCode(String billCode) {
		this.billCode = billCode;
	}
	public String getGoodsNm() {
		return goodsNm;
	}
	public void setGoodsNm(String goodsNm) {
		this.goodsNm = goodsNm;
	}
	
}

package com.qxh.model;

public class PurchaseTotalDetail {
	private String code;
	private String goodsNm;
	private Double orderNum;
	private String customNm;

	public PurchaseTotalDetail() {
		super();
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getGoodsNm() {
		return goodsNm;
	}

	public void setGoodsNm(String goodsNm) {
		this.goodsNm = goodsNm;
	}

	public Double getOrderNum() {
		return orderNum;
	}

	public void setOrderNum(Double orderNum) {
		this.orderNum = orderNum;
	}

	public String getCustomNm() {
		return customNm;
	}

	public void setCustomNm(String customNm) {
		this.customNm = customNm;
	}

	@Override
	public String toString() {
		return "PurchaseTotalDetail [code=" + code + ", goodsNm=" + goodsNm + ", orderNum=" + orderNum + ", customNm="
				+ customNm + "]";
	}

}

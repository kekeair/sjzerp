package com.qxh.exmodel;

import com.qxh.utils.NumUtil;

public class DemandSummaryD {
	
	private String goodsNm;
	private String billDate;
	private String customerNm;
	private double demandNum;
	private String unitNm;
	private double price;
	
	public String getGoodsNm() {
		return goodsNm;
	}
	public void setGoodsNm(String goodsNm) {
		this.goodsNm = goodsNm;
	}
	public String getBillDate() {
		return billDate;
	}
	public void setBillDate(String billDate) {
		this.billDate = billDate;
	}
	public String getCustomerNm() {
		return customerNm;
	}
	public void setCustomerNm(String customerNm) {
		this.customerNm = customerNm;
	}
	public double getDemandNum() {
		return demandNum;
	}
	public void setDemandNum(double demandNum) {
		this.demandNum = demandNum;
	}
	public String getUnitNm() {
		return unitNm;
	}
	public void setUnitNm(String unitNm) {
		this.unitNm = unitNm;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = NumUtil.trim0_4(price);
	}
}

package com.qxh.utils;

import java.io.Serializable;

import com.qxh.constant.CodeConstant;

/**
 * @ClassName: Result
 * @Description: 返回接口的标准类
 * @author chenyang
 * @date 2014-12-2 下午3:46:14
 * 
 */
public class Result implements Serializable {
	private int code = CodeConstant.CODE1000;
	private String msg = CodeConstant.CODE1000_SUCCESS;
	private Object data;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}
}
